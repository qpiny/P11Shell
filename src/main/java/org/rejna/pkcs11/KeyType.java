package org.rejna.pkcs11;

public enum KeyType {
	CKK_RSA(0x00000000),
	CKK_DSA(0x00000001),
	CKK_DH(0x00000002),
	CKK_ECDSA(0x00000003),
	CKK_EC(0x00000003),
	CKK_X9_42_DH(0x00000004),
	CKK_KEA(0x00000005),
	CKK_GENERIC_SECRET(0x00000010),
	CKK_RC2(0x00000011),
	CKK_RC4(0x00000012),
	CKK_DES(0x00000013),
	CKK_DES2(0x00000014),
	CKK_DES3(0x00000015),
	CKK_CAST(0x00000016),
	CKK_CAST3(0x00000017),
	CKK_CAST5(0x00000018),
	CKK_CAST128(0x00000018),
	CKK_RC5(0x00000019),
	CKK_IDEA(0x0000001A),
	CKK_SKIPJACK(0x0000001B),
	CKK_BATON(0x0000001C),
	CKK_JUNIPER(0x0000001D),
	CKK_CDMF(0x0000001E),
	CKK_AES(0x0000001F),
	CKK_VENDOR_DEFINED(0x80000000);
	
	private int value;
	
	private KeyType(int value) {
		this.value = value;
	}
	
	public int getValue() {
		return value;
	}
}
